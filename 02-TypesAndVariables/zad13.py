import math
#####
# Calculation of the area and circumference of a circle
##

# determine radius and PI
r = 5
pi = math.pi

# calculate area 
area = r**2*pi

# calculate circumference 
circ = 2*r*pi

# display results 
print(f"The circle's area is: {area}")

print(f"The circle's circumference is: {circ}")



